public class Klasa {
    private int idKlasy;
    private String nazwaKlasy;

    public Klasa(int idKlasy, String nazwaKlasy) {
        this.idKlasy = idKlasy;
        this.nazwaKlasy = nazwaKlasy;
    }

    public int getIdKlasy() {
        return idKlasy;
    }

    public String getNazwaKlasy() {
        return nazwaKlasy;
    }
}